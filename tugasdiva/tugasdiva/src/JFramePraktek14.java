    private void setStatusMenikah(boolean status) {
        
        // check if pegawai is null
        if (pegawai == null) {
            radioGroup.clearSelection();
            JOptionPane.showMessageDialog(rootPane, "Mohon pilih jabatan terlebih dahulu!");
            return;
        }
        
        // update pegawai jumlah anak
        int jumlahAnak = Integer.parseInt(textFieldJumlahAnak.getText());
        pegawai.setJumlahAnak(jumlahAnak);
        
        // update pegawai status menikah
        pegawai.setStatusMenikah(status);
        
        // enable text field jumlah anak
        textFieldJumlahAnak.setEditable(status);
        
        // hapus data output lama
        textFieldGajiKotor.setText("Rp. 0");
        textFieldPajak.setText("Rp. 0");
        textFieldGajiBersih.setText("Rp. 0");
        
        // tampilkan output data
        calculateOutput();
    }
    
    private void calculateOutput() {
        textFieldJumlahAnak.setText(String.format("%d", pegawai.getJumlahAnak()));
        textFieldTunjanganAnak.setText(String.format("Rp. %d", pegawai.getTunjanganAnak()));
        textFieldTunjanganIstri.setText(String.format("Rp. %d", pegawai.getTunjanganIstri()));
    }
    
    private void calculateTotal() {
        int gajiBersih = pegawai.getGajiPokok() + pegawai.getTunjanganAnak() + pegawai.getTunjanganIstri();
        int pajak = pegawai.getPajak();
        textFieldGajiKotor.setText(String.format("Rp. %d", gajiBersih));
        textFieldPajak.setText(String.format("Rp. %d", pajak));
        textFieldGajiBersih.setText(String.format("Rp. %d", gajiBersih - pajak));
    }
    
    private void clearOutput() {
        pegawai = null;
        textFieldGajiPokok.setText("Rp. 0");
        textFieldJumlahAnak.setText("0");
        textFieldTunjanganAnak.setText("Rp. 0");
        textFieldTunjanganIstri.setText("Rp. 0");
        textFieldGajiKotor.setText("Rp. 0");
        textFieldPajak.setText("Rp. 0");
        textFieldGajiBersih.setText("Rp. 0");
    }
    
