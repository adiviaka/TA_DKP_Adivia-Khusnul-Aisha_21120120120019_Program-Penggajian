    private void comboBoxJabatanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBoxJabatanActionPerformed
        if (comboBoxJabatan.getSelectedIndex() != 0) {
            if (textFieldNIP.getText().isEmpty()) {
                comboBoxJabatan.setSelectedIndex(0);
                JOptionPane.showMessageDialog(rootPane, "Mohon isi NIP Anda!");
                return;
            }
            if (textFieldName.getText().isEmpty()) {
                comboBoxJabatan.setSelectedIndex(0);
                JOptionPane.showMessageDialog(rootPane, "Mohon isi nama Anda!");
                return;
            }
            String nip = textFieldNIP.getText();
            String name = textFieldName.getText();
            Jabatan jabatan = new Jabatan(String.valueOf(comboBoxJabatan.getSelectedItem()));
            pegawai = new Pegawai(nip, name, jabatan);
            textFieldGajiPokok.setText(String.format("Rp. %d", pegawai.getGajiPokok()));
            radioGroup.clearSelection();
            textFieldJumlahAnak.setEditable(false);
            textFieldJumlahAnak.setText("0");
            textFieldTunjanganIstri.setText("Rp. 0");
        } else {
            clearOutput();
        }
    }//GEN-LAST:event_comboBoxJabatanActionPerformed

